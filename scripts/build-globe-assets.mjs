import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const sourcePath = join(here, "source", "world-countries.json");
const outputPath = join(here, "..", "src", "generated", "globe-land-dots.ts");

const DEG = Math.PI / 180;
const LAND_DOT_RADIUS = 1.0025;
const LAND_LAT_STEP = 0.78;
const LAND_LNG_STEP = 0.82;
const BASE_COLOR = hexToRgb("#0d414a");
const LIFT_COLOR = hexToRgb("#145d60");

const countries = JSON.parse(readFileSync(sourcePath, "utf8"));
const polygons = normalizeCountries(countries);
const { positions, colors } = createLandDotAttributes(polygons);

writeFileSync(
  outputPath,
  [
    "/* Generated by scripts/build-globe-assets.mjs. Do not edit by hand. */",
    "",
    `export const globeLandDotCount = ${positions.length / 3};`,
    "",
    "export const globeLandDotPositions = [",
    formatArray(positions),
    "];",
    "",
    "export const globeLandDotColors = [",
    formatArray(colors),
    "];",
    "",
  ].join("\n"),
);

console.log(`Generated ${positions.length / 3} globe land dots`);

function normalizeCountries(input) {
  const output = [];

  for (const feature of input.features ?? []) {
    const geometry = feature.geometry;
    if (!geometry) continue;

    if (geometry.type === "Polygon") {
      addPolygon(output, geometry.coordinates);
      continue;
    }

    if (geometry.type === "MultiPolygon") {
      for (const polygon of geometry.coordinates) {
        addPolygon(output, polygon);
      }
    }
  }

  return output;
}

function addPolygon(polygons, rings) {
  const exterior = rings[0];
  if (!exterior?.length) return;

  polygons.push({
    rings,
    bounds: getBounds(exterior),
  });
}

function getBounds(ring) {
  let minLng = Infinity;
  let maxLng = -Infinity;
  let minLat = Infinity;
  let maxLat = -Infinity;

  for (const [lng, lat] of ring) {
    minLng = Math.min(minLng, lng);
    maxLng = Math.max(maxLng, lng);
    minLat = Math.min(minLat, lat);
    maxLat = Math.max(maxLat, lat);
  }

  return { minLng, maxLng, minLat, maxLat };
}

function pointInRing(lng, lat, ring) {
  let inside = false;

  for (let i = 0, j = ring.length - 1; i < ring.length; j = i++) {
    const [lngI, latI] = ring[i];
    const [lngJ, latJ] = ring[j];
    const crosses = latI > lat !== latJ > lat;

    if (!crosses) continue;

    const projectedLng =
      ((lngJ - lngI) * (lat - latI)) / (latJ - latI + Number.EPSILON) + lngI;

    if (lng < projectedLng) inside = !inside;
  }

  return inside;
}

function isLand(lng, lat, polygons) {
  for (const polygon of polygons) {
    const { bounds, rings } = polygon;
    if (
      lng < bounds.minLng ||
      lng > bounds.maxLng ||
      lat < bounds.minLat ||
      lat > bounds.maxLat
    ) {
      continue;
    }

    if (!pointInRing(lng, lat, rings[0])) continue;

    for (let i = 1; i < rings.length; i += 1) {
      if (pointInRing(lng, lat, rings[i])) return false;
    }

    return true;
  }

  return false;
}

function latLngToVector(lat, lng, radius) {
  const latRad = lat * DEG;
  const lngRad = lng * DEG;
  const cosLat = Math.cos(latRad);

  return [
    radius * cosLat * Math.cos(lngRad),
    radius * Math.sin(latRad),
    radius * cosLat * Math.sin(lngRad),
  ];
}

function createLandDotAttributes(polygons) {
  const positions = [];
  const colors = [];
  let row = 0;

  for (let lat = -83.6; lat <= 83.6; lat += LAND_LAT_STEP) {
    const cosLat = Math.max(Math.cos(lat * DEG), 0.16);
    const count = Math.max(20, Math.round((360 / LAND_LNG_STEP) * cosLat));
    const offset = row % 2 === 0 ? 0 : 0.5;

    for (let index = 0; index < count; index += 1) {
      const lng = -180 + ((index + offset) / count) * 360;
      if (!isLand(lng, lat, polygons)) continue;

      positions.push(...latLngToVector(lat, lng, LAND_DOT_RADIUS));

      const regionalTint = Math.max(0, Math.sin((lat + 12) * DEG)) * 0.16;
      colors.push(...lerpColor(BASE_COLOR, LIFT_COLOR, regionalTint));
    }

    row += 1;
  }

  return { positions, colors };
}

function hexToRgb(hex) {
  const value = Number.parseInt(hex.slice(1), 16);
  return [
    ((value >> 16) & 255) / 255,
    ((value >> 8) & 255) / 255,
    (value & 255) / 255,
  ];
}

function lerpColor(from, to, t) {
  return from.map((channel, index) => channel + (to[index] - channel) * t);
}

function formatArray(values) {
  const chunks = [];
  for (let index = 0; index < values.length; index += 12) {
    chunks.push(`  ${values.slice(index, index + 12).map(formatNumber).join(", ")},`);
  }
  return chunks.join("\n");
}

function formatNumber(value) {
  return Number(value.toFixed(6)).toString();
}
